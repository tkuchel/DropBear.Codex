namespace DropBear.Codex.Blazor.Enums;

/// <summary>
///     Visual variants for progress components.
/// </summary>
public enum ProgressVariant
{
    /// <summary>
    ///     Primary brand color variant.
    /// </summary>
    Primary,

    /// <summary>
    ///     Secondary color variant.
    /// </summary>
    Secondary,

    /// <summary>
    ///     Success state variant (typically green).
    /// </summary>
    Success,

    /// <summary>
    ///     Warning state variant (typically yellow/orange).
    /// </summary>
    Warning,

    /// <summary>
    ///     Error state variant (typically red).
    /// </summary>
    Error,

    /// <summary>
    ///     Information state variant (typically blue).
    /// </summary>
    Info,

    /// <summary>
    ///     Neutral/muted variant.
    /// </summary>
    Neutral
}
